public class Demo07 {
    public static void main(String[] args) {
        /*
    关系运算符：> < >= <= == !=
    运算结果为布尔
     */
        int a = 10;
        int b = 10;
        boolean r1 = a > b;
        boolean r2 = a >= b;
        boolean r3 = a < b;
        boolean r4 = a <= b;
        boolean r5 = a == b;
        boolean r6 = a != b;
        System.out.println(r1);
        System.out.println(r2);
        System.out.println(r3);
        System.out.println(r4);
        System.out.println(r5);
        System.out.println(r6);
    }
}
