public class Demo20 {
    public static void main(String[] args) {
        // 正方形
        // 二重循环 外循环行 内循环*
        int number = 5;
        for (int i = 0; i < number; i++) {
            for (int j = 0; j < number; j++) {
                System.out.print("*");
            }
            System.out.print("\n");
        }


    }
}
